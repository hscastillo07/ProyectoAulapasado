package Proceso;

public class Series {

    public int Codigo;

    public String Descripcion;

    public Series() {
    }

    public int getCodigo() {
        return Codigo;
    }

    public String getDescripcion() {
        return Descripcion;
    }

    public void setCodigo() {
        this.Codigo = Codigo;
    }

    public void setDescripcion() {
        this.Descripcion = Descripcion;
    }
}
